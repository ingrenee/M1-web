<h2>¿Qué es hayempleo.com?</h2>
<p>hayempleo.com es una plataforma web gratuita y concebida para  la publicación de empleos y trabajos.</p>
<p>&nbsp;</p>
<h2>¿Cómo se financia hayempleo.com ?</h2>
<p>Es financiada con fondos personales de los propios  desarrolladores de sistemas que lo implementaron, quienes han puesto sus  conocimientos y experiencia gratuitamente con la creación de hayempleo.com para  todos ustedes.</p>
<p><br />
  Hayempleo.com no es <strong>una empresa de selección personal, ni de  recursos humanos.</strong></p>
<p><br />
  Hayempleo.com es una <strong>herramienta gratuita.</strong></p>
<p>&nbsp;</p>
<h2>
  Visión: </h2>
<p>  Nuestra visión es convertir a hayempleo.com en la web de  empleos y trabajos más visitada del Perú.</p>
<p>&nbsp;</p>
<h2>
  Misión:</h2>
<p>  Brindar a las pequeñas, mediadas, grandes empresas o a  cualquiera que tenga un puesto de trabajo la posibilidad de publicar sus  ofertas de empleo sin costo alguno.</p>
<p><br />
  Y a los candidatos darles un sitio web donde estén seguros  de que podrán encontrar el puesto de trabajo ideal.</p>
